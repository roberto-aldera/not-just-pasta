                <h3>Instructions</h3>
                    <li>Heat olive oil in casserole pot over medium/high heat, then add onion and cook until transparent.</li>
                    <li>Add mince and cook until browned.</li>
                    <li>Add the peppers, garlic and all of the spices; mix and cook until peppers soften (approx. 10 minutes).</li>
                    <li>Add chopped tomatoes, passata, olives and beans and bring to the boil. Reduce to a simmer, cover and cook for 10 minutes, stirring occasionally.</li>
                    <li>Serve with rice and toppings of your choice.</li>   
                </ol>
