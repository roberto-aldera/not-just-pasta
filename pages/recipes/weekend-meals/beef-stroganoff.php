                <h3>Ingredients</h3>
                
                    <ul>
                    <li>Olive oil</li>
                    <li>450g sirloin steak, cut across the grain into strips</li>
                    <li>1 onion, diced</li>
                    <li>450g mushrooms, sliced</li>
                    <li>2 cloves garlic, minced</li>
                    <li>2 Tbsp butter</li>
                    <li>2 Tbsp plain flour</li>
                    <li>1 &frac12; cups beef broth</li>
                    <li>5ml dried thyme, or 5 sprigs fresh thyme</li>
                    <li>1 Tbsp Dijon or grain mustard</li>
                    <li>&frac12; cup Greek yoghurt</li>
                    <li>1 mug of rice</li>
                    <li>Choice veggies (like fresh French beans you've grown yourself and are quite proud of)</li>
                    </ul>
                
                <h3>Instructions</h3>
                <ol>
                    <li>Heat olive oil in large saucepan; add meat and some salt and pepper.</li>
                    <li>Allow to brown for approximately 1 minute and remove from pan.</li>
                    <li>Reduce heat to medium; add onion and allow to soften for 2-3 minutes, then add garlic and mushrooms.</li>
                    <li>Cook until all vegetables are soft and remove from pan.</li>
                    <li>Add butter and allow to melt, then sprinkle flour over, mix and cook for 1 minute.</li>
                    <li>Add beef broth, stir and cook for 2 minutes to allow sauce to thicken.</li>
                    <li>Add thyme, mustard and yoghurt and mix, then return meat and vegetables to the pan.</li>
                    <li>Stir and allow to cook at low heat for 10 minutes.</li>
                    <li>Serve with rice (or potatoes) and vegetables of choice.</li>
                </ol>
                <p>
                    Serves 5.
                </p>
