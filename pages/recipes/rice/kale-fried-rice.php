                <h3>Ingredients</h3>
                <ul>
                    <li>3 Tbsp butter, divided</li>
                    <li>4 spring onions, thinly sliced</li>
                    <li>3 cloves garlic, minced</li>
                    <li>2 Tbsp fresh ginger, grated</li>
                    <li>4 cups cooked and chilled rice (try brown rice)</li>
                    <li>1 cup frozen peas</li>
                    <li>125g kale (a small bunch)</li>
                    <li>&frac14; cup soy sauce</li>
                    <li>&frac12; tsp toasted sesame oil</li>
                    <li>3 eggs, whisked</li>
                    <li>Ground black pepper, to taste</li>
                </ul>
                <ol>
                      
   
                    <li>Melt 1 Tbsp of the butter in a pan over medium-high heat.
                        Saut√© onions for 2 minutes, stirring occasionally.
                        Add garlic and ginger and cook for 1 minute, stirring often.
                    </li>    
                    <li>Add remaining butter, then add rice and spread out evenly.
                        Cook undisturbed for 2 minutes, then stir and cook for another 2 minutes; repeat until rice is a little crispy.
                    </li>
                    <li>Add the peas and stir, then add the kale bit by bit and allow it to wilt (cover with lid to speed up the process).
                        Drizzle the soy sauce and sesame oil over and mix.
                    </li>
                    <li>Move food aside and scramble eggs in that space, then mix and season to taste.</li>
                </ol>
                <p>
                    Serves 4.
