            <div class="col-sm-6">
                <h3>Ingredients</h3>
                <ul>
                    <li>&frac12 of a whole butternut (~600g) or pumpkin</li>
                    <li>300g chestnut mushrooms</li>
                    <li>1 large onion</li>
                    <li>1 litre vegetable stock</li>
                    <li>4 garlic cloves</li>
                    <li>12 sage leaves</li>
                    <li>300g pearl barley</li>
                    <li>Parmigiano</li>
                </ul>
                <ol>
                       
                    <li>Heat some olive oil in a casserole dish, then add the butternut and cook on high for 10 min to brown the edges;
                        remove with a slotted spoon and set aside.
                    </li>    
                    <li>Add some more olive oil, then brown the mushrooms for 6-8 min, stirring occasionally.
                        Set aside with the butternut.
                    </li>
                    <li>Top up with oil again if needed and add the onion and cook until golden brown (8-12 minutes); make the vegetable stock.</li>
                    <li>Add the garlic, sage and pearl barley to the pot and stir, then add the stock, bring to a boil and cook for 10 min.</li>
                    <li>Reduce to a simmer, add the butternut and mushrooms and cook for 40 minutes partially covered, stirring occasionally.
                    </li>
                    <li>Check the pearl barley and cook for a further 5-10 min if not done yet (you can also add water if it is drying out).</li>
                    <li>Mix through the parmiggiano and serve topped with olive oil, sage leaves and parmiggiano.</li>
                </ol>
                <p>
                    Serves 4.
                </p>
