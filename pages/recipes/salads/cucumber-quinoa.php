                <h3>Ingredients</h3>
                <h5>Salad</h5>
                <ul>
                    <li>1 English cucumber, diced</li>
                    <li>2 cups chilled, cooked quinoa*</li>
                    <li>&frac12; cup crumbled feta cheese (or mini mozzarella balls)</li>
                    <li>&frac13; cup basil leaves, chopped</li>
                    <li>1 batch lemony vinaigrette</li>
                </ul>

                <h5>Lemony vinaigrette</h5>
                <ul>
                    <li>&frac14; cup olive oil</li>
                    <li>2 Tbsp apple cider vinegar or red wine vinegar</li>
                    <li>1 Tbsp fresh lemon juice</li>
                    <li>&frac12; tsp Italian seasoning (I use mixed/Italian herbs)</li>
                    <li>Pinch of salt and black pepper</li>
                </ul>

                <h3>Instructions</h3>
                <ol>
                    <li>Rinse quinoa thoroughly and drain, then add to boiling water with some salt.
                        Reduce to a simmer and cook for 12-15 minutes, or until translucent and tender.
                    </li>    
                    <li>Drain the quinoa, return to pan and cover with a tea towel and steam for 5-10 min off the heat. Allow to cool.</li>
                    <li>Toss all of the salad ingredients together.</li>
                    <li>Whisk all of the vinaigrette ingredients together until combined, then toss with the salad.</li>
                    <li>Serve with meat portion of choice.</li>
                </ol>
                <p>
                *The ratio used is 1 cup uncooked quinoa plus 3 cups water, to yield 3 cups of cooked quinoa. Adjust ratios accordingly to get 2 cups of cooked quinoa
                </p>
                <p>    
                    Serves 4.
                </p>
