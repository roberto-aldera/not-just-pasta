            <div class="col-sm-6">
                <h3>Ingredients</h3>
                <h5>Pesto:</h5>
                <ul>
                    <li>50g pine nuts</li>
                    <li>&frac14 cup grated parmigiano</li>
                    <li>2 garlic cloves</li>
                    <li>&frac12 tsp salt</li>
                    <li>&frac12 cup olive oil</li>
                    <li>2 large bunches of fresh basil</li>
                </ul>

                <h5>Main:</h5>
                <ul>
                    <li>350g farfalle</li>
                    <li>400-500g mushrooms</li>
                    <li>1 onion</li>
                    <li>150g pancetta</li>
                    <li>2 tbsp fresh pesto</li>
                </ul>

                <h3>Instructions</h3>
                <h5>Pesto:</h5>
                <ol>
                    <li>Roast the pinenuts in a pot (the one you use later for the pasta is fine).</li>
                    <li>Add roasted pinenuts to the salt and garlic to grind them all together.</li>
                    <li>Add the basil, cheese, and oil in small quantities and pound them as you go.</li>
                </ol>

                <h5>Main:</h5>
                <ol>
                    <li>Dice onion and fry it up in olive oil with the pancetta. It's a good idea to put the pasta water on now too.</li>
                    <li>Once they have softened, add in the freshly chopped mushrooms and cover.</li>
                    <li>After 10-15 minutes, add in 2-4 tbsp of fresh pesto, and turn off the heat as you fold it in.
                        Cooking it for too long reduces the flavour and the punch of the garlic/basil.
                    </li>
                    <li>Add the cooked pasta into the mix before serving.</li>
                </ol>
                <p>
                    Serves 4 + pesto for another time.
                </p>
