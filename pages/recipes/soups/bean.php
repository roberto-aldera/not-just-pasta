                <h3>Ingredients</h3>
                <ul>
                    <li>5ml oil</li>
                    <li>1 large onion</li>
                    <li>150g pancetta</li>
                    <li>230g mixed dry beans (or 2 tins)</li>
                    <li>500ml water</li>
                    <li>4 tbsp oats</li>
                    <li>250ml milk</li>
                    <li>1 bunch basil</li>
                    <li>1 tsp lemon juice</li>
                </ul>
                <ol>
                    <li>Heat oil in large saucepan, fry the onion until transparent.</li>    
                    <li>Add the pancetta and fry until crisp.</li>
                    <li>Add the cooked (or canned) beans with their liquid (or the cooked beans with a little water), the 2 cups of water and season.
                    Boil (covered) for 30 minutes.
                    </li>
                    <li>Mash the beans, and add the oats, milk and basil; cook (covered) for 15 minutes.</li>
                    <li>Add the lemon juice and stir until well combined. Serve with fresh bread if desired.</li>
                </ol>
                <p>
                    Serves 4.
                </p>
